/*
  # Legal Document Portal Schema

  ## Purpose
  Supports the Legal Document Portal — a workspace for attorneys and paralegals to draft
  pleadings from templates, manage ECF inbox entries, and auto-generate tasks + calendar
  deadlines triggered by ECF filing types.

  ## New Tables

  ### 1. `ecf_inbox`
  Stores incoming ECF (Electronic Case Filing) notices/docket entries.
  - `id` — primary key
  - `client_id` — links to clients table
  - `case_number` — court case number
  - `docket_entry` — text description of the docket entry
  - `filing_type` — categorized type (e.g., motion_for_relief, objection, order, notice)
  - `filed_by` — filer name/entity
  - `filed_date` — date filed in court
  - `deadline_days` — auto-computed response window in days
  - `status` — pending | task_created | responded | dismissed
  - `created_at`

  ### 2. `pleading_templates`
  Master list of template pleadings available for a given case type.
  - `id`
  - `name` — display name (e.g., "Response to Motion for Relief from Stay")
  - `category` — change_of_address | motion_response | objection_response | general
  - `applicable_filing_type` — which ECF filing type triggers this template
  - `content_template` — the full pleading text with {{variable}} placeholders
  - `created_at`

  ### 3. `pleading_drafts`
  Saved or submitted drafts generated from templates for a specific client/case.
  - `id`
  - `client_id`
  - `template_id` — FK to pleading_templates
  - `ecf_inbox_id` — optional link to the ECF entry that triggered this
  - `case_number`
  - `title`
  - `content` — rendered/edited pleading text
  - `status` — draft | under_review | filed | archived
  - `created_by` — staff name
  - `created_at`
  - `updated_at`

  ### 4. `ecf_rules`
  Rules engine: maps ECF filing types to auto-tasks + deadline offsets.
  - `id`
  - `filing_type` — matches ecf_inbox.filing_type
  - `rule_name` — human label
  - `task_title` — task to auto-create
  - `deadline_days` — days from filing date to respond
  - `priority` — high | medium | low
  - `auto_calendar_event` — whether to push a calendar event
  - `template_id` — suggested template FK (nullable)
  - `is_active`

  ### 5. `ecf_tasks`
  Tasks auto-generated by the rules engine when an ECF entry arrives.
  - `id`
  - `ecf_inbox_id` — FK
  - `client_id`
  - `rule_id` — FK to ecf_rules
  - `title`
  - `due_date`
  - `priority`
  - `assigned_to`
  - `status` — open | in_progress | completed | dismissed
  - `calendar_event_created`
  - `created_at`

  ## Security
  - RLS enabled on all tables
  - Authenticated users can read/write all rows (internal staff tool)
*/

-- ── ecf_inbox ──────────────────────────────────────────────────────────────────
CREATE TABLE IF NOT EXISTS ecf_inbox (
  id uuid PRIMARY KEY DEFAULT gen_random_uuid(),
  client_id text,
  case_number text NOT NULL DEFAULT '',
  docket_entry text NOT NULL DEFAULT '',
  filing_type text NOT NULL DEFAULT 'general',
  filed_by text NOT NULL DEFAULT '',
  filed_date date NOT NULL DEFAULT CURRENT_DATE,
  deadline_days integer DEFAULT 14,
  status text NOT NULL DEFAULT 'pending',
  created_at timestamptz DEFAULT now()
);

ALTER TABLE ecf_inbox ENABLE ROW LEVEL SECURITY;

CREATE POLICY "Authenticated staff can read ecf_inbox"
  ON ecf_inbox FOR SELECT TO authenticated USING (true);

CREATE POLICY "Authenticated staff can insert ecf_inbox"
  ON ecf_inbox FOR INSERT TO authenticated WITH CHECK (true);

CREATE POLICY "Authenticated staff can update ecf_inbox"
  ON ecf_inbox FOR UPDATE TO authenticated USING (true) WITH CHECK (true);

-- ── pleading_templates ─────────────────────────────────────────────────────────
CREATE TABLE IF NOT EXISTS pleading_templates (
  id uuid PRIMARY KEY DEFAULT gen_random_uuid(),
  name text NOT NULL DEFAULT '',
  category text NOT NULL DEFAULT 'general',
  applicable_filing_type text,
  content_template text NOT NULL DEFAULT '',
  created_at timestamptz DEFAULT now()
);

ALTER TABLE pleading_templates ENABLE ROW LEVEL SECURITY;

CREATE POLICY "Authenticated staff can read pleading_templates"
  ON pleading_templates FOR SELECT TO authenticated USING (true);

CREATE POLICY "Authenticated staff can insert pleading_templates"
  ON pleading_templates FOR INSERT TO authenticated WITH CHECK (true);

CREATE POLICY "Authenticated staff can update pleading_templates"
  ON pleading_templates FOR UPDATE TO authenticated USING (true) WITH CHECK (true);

-- ── pleading_drafts ────────────────────────────────────────────────────────────
CREATE TABLE IF NOT EXISTS pleading_drafts (
  id uuid PRIMARY KEY DEFAULT gen_random_uuid(),
  client_id text,
  template_id uuid REFERENCES pleading_templates(id) ON DELETE SET NULL,
  ecf_inbox_id uuid REFERENCES ecf_inbox(id) ON DELETE SET NULL,
  case_number text NOT NULL DEFAULT '',
  title text NOT NULL DEFAULT '',
  content text NOT NULL DEFAULT '',
  status text NOT NULL DEFAULT 'draft',
  created_by text NOT NULL DEFAULT '',
  created_at timestamptz DEFAULT now(),
  updated_at timestamptz DEFAULT now()
);

ALTER TABLE pleading_drafts ENABLE ROW LEVEL SECURITY;

CREATE POLICY "Authenticated staff can read pleading_drafts"
  ON pleading_drafts FOR SELECT TO authenticated USING (true);

CREATE POLICY "Authenticated staff can insert pleading_drafts"
  ON pleading_drafts FOR INSERT TO authenticated WITH CHECK (true);

CREATE POLICY "Authenticated staff can update pleading_drafts"
  ON pleading_drafts FOR UPDATE TO authenticated USING (true) WITH CHECK (true);

-- ── ecf_rules ──────────────────────────────────────────────────────────────────
CREATE TABLE IF NOT EXISTS ecf_rules (
  id uuid PRIMARY KEY DEFAULT gen_random_uuid(),
  filing_type text NOT NULL DEFAULT '',
  rule_name text NOT NULL DEFAULT '',
  task_title text NOT NULL DEFAULT '',
  deadline_days integer NOT NULL DEFAULT 14,
  priority text NOT NULL DEFAULT 'medium',
  auto_calendar_event boolean NOT NULL DEFAULT true,
  template_id uuid REFERENCES pleading_templates(id) ON DELETE SET NULL,
  is_active boolean NOT NULL DEFAULT true,
  created_at timestamptz DEFAULT now()
);

ALTER TABLE ecf_rules ENABLE ROW LEVEL SECURITY;

CREATE POLICY "Authenticated staff can read ecf_rules"
  ON ecf_rules FOR SELECT TO authenticated USING (true);

CREATE POLICY "Authenticated staff can insert ecf_rules"
  ON ecf_rules FOR INSERT TO authenticated WITH CHECK (true);

CREATE POLICY "Authenticated staff can update ecf_rules"
  ON ecf_rules FOR UPDATE TO authenticated USING (true) WITH CHECK (true);

-- ── ecf_tasks ──────────────────────────────────────────────────────────────────
CREATE TABLE IF NOT EXISTS ecf_tasks (
  id uuid PRIMARY KEY DEFAULT gen_random_uuid(),
  ecf_inbox_id uuid REFERENCES ecf_inbox(id) ON DELETE CASCADE,
  client_id text,
  rule_id uuid REFERENCES ecf_rules(id) ON DELETE SET NULL,
  title text NOT NULL DEFAULT '',
  due_date date,
  priority text NOT NULL DEFAULT 'medium',
  assigned_to text NOT NULL DEFAULT '',
  status text NOT NULL DEFAULT 'open',
  calendar_event_created boolean NOT NULL DEFAULT false,
  created_at timestamptz DEFAULT now()
);

ALTER TABLE ecf_tasks ENABLE ROW LEVEL SECURITY;

CREATE POLICY "Authenticated staff can read ecf_tasks"
  ON ecf_tasks FOR SELECT TO authenticated USING (true);

CREATE POLICY "Authenticated staff can insert ecf_tasks"
  ON ecf_tasks FOR INSERT TO authenticated WITH CHECK (true);

CREATE POLICY "Authenticated staff can update ecf_tasks"
  ON ecf_tasks FOR UPDATE TO authenticated USING (true) WITH CHECK (true);

